            <h2>Memulai dengan Kasku</h2>
            <p>Sebelum Anda mencoba menggunakan web app KasKu, penting bagi Anda untuk mengenal dan memahami berbagai halaman yang ada dalam perangkat lunak kami. Hal ini akan membantu Anda memanfaatkan aplikasi dengan lebih baik dan memahami semua fitur dan fungsi yang tersedia</p>
            <h3>Pengenalan antar muka</h3>
            <h4>Dashboard</h4>
            <figure><img src="image/mobile.png" alt="">
                <figcaption></figcaption>
            </figure>
            <ul>
                <li>Dasbor kami memberikan pandangan sekilas tentang situasi keuangan Anda, termasuk saldo saat ini, pengeluaran bulan ini, dan progress terhadap anggaran Anda.</li>
                <li>Pada dasbor, Anda dapat dengan cepat melihat grafik dan grafik lingkaran yang memberikan gambaran visual tentang bagaimana uang Anda telah digunakan dalam beberapa kategori pengeluaran utama.</li>
                <li>Kami menyediakan ringkasan keuangan yang mudah dipahami di dasbor, sehingga Anda dapat dengan mudah melacak perkembangan keuangan Anda seiring berjalannya waktu.</li>
                <li>Dasbor kami dirancang untuk memberikan visibilitas penuh atas keuangan Anda, membantu Anda membuat keputusan keuangan yang cerdas, dan mencapai tujuan keuangan Anda.</li>
                <li>Anda dapat melacak perkembangan keuangan Anda dengan mudah melalui grafik dan statistik yang interaktif di dasbor kami.</li>
                <li>Dasbor kami memberikan laporan real-time tentang aset dan kewajiban Anda, memungkinkan Anda untuk tetap terinformasi dan mengambil tindakan yang tepat.</li>
                <li>Dengan dasbor yang user-friendly ini, Anda dapat dengan mudah menjelajahi data keuangan Anda dan membuat keputusan finansial yang lebih terinformasi.</li>
              </ul>
            <h4>Transaksi</h4>
            <figure><img src="image/mobile (1).png" alt="">
                <figcaption></figcaption>
            </figure>
            <ul>
                <li>Halaman Transaksi adalah tempat Anda dapat mencatat setiap transaksi keuangan Anda, baik itu pengeluaran atau pemasukan. Silakan tambahkan detail transaksi Anda di sini.</li>
                <li>Ingin memantau bagaimana uang Anda digunakan? Gunakan halaman Transaksi untuk mengatur dan melacak semua aktivitas keuangan Anda dalam satu tempat.</li>
                <li>Tambahkan transaksi baru dengan mengklik tombol 'Tambah Transaksi' di halaman ini. Pastikan untuk memasukkan tanggal, jumlah, dan kategori yang sesuai.</li>
                <li>Jika Anda perlu mencari transaksi tertentu, gunakan fitur pencarian di halaman Transaksi untuk menemukan informasi yang Anda butuhkan.</li>
                <li>Jika ada transaksi yang perlu diedit atau dihapus, Anda dapat melakukannya dengan mudah dari halaman ini.</li>
                <li>Selalu pastikan untuk mencatat setiap transaksi Anda di sini agar Anda memiliki gambaran yang akurat tentang keuangan pribadi Anda.</li>
            </ul>
            
            <h4>Kategori Transaksi</h4>
            <figure><img src="image/mobile (15).png" alt="">
                <figcaption></figcaption>
            </figure>
            <ul>
                <li>Halaman Kategori Transaksi memungkinkan Anda untuk mengatur dan menyesuaikan kategori-kategori yang digunakan untuk mengelompokkan transaksi Anda.</li>
                <li>Anda dapat membuat, mengedit, atau menghapus kategori-kategori transaksi sesuai dengan kebutuhan Anda di halaman ini.</li>
                <li>Jika Anda ingin menambahkan kategori baru, cukup klik tombol 'Tambah Kategori' di halaman ini dan beri nama kategori tersebut.</li>
                <li>Dengan mengatur kategori transaksi dengan baik, Anda dapat melacak pengeluaran dan pemasukan dalam setiap aspek kehidupan keuangan Anda.</li>
                <li>Jika ada perubahan dalam kebutuhan atau prioritas keuangan Anda, Anda dapat selalu mengedit atau menghapus kategori yang ada.</li>
                <li>Penting untuk memastikan kategori-kategori transaksi Anda mencerminkan keuangan Anda dengan akurat dan sesuai dengan rencana keuangan Anda.</li>
                <li>Dengan mengelola kategori-kategori transaksi dengan baik, Anda dapat membuat laporan keuangan yang lebih rinci dan merencanakan anggaran yang lebih efektif.</li>
            </ul>
            
            <h4>Anggaran</h4>
            <figure><img src="image/mobile (6).png" alt="">
                <figcaption></figcaption>
            </figure>
            <ul>
                <li>Halaman Anggaran memungkinkan Anda untuk merencanakan dan mengelola alokasi keuangan Anda berdasarkan prinsip yang sehat: 50% untuk kebutuhan, 30% untuk keinginan, dan 20% untuk tabungan, atau anda dapat mengkostumisasi alokasi keuangnnya</li>
                <li>Anda dapat dengan mudah membuat anggaran dengan memasukkan pengeluaran yang terkait dengan kebutuhan sehari-hari Anda di dalam kategori 50%.</li>
                <li>Bagi 30% sisanya untuk keinginan Anda, seperti hobi atau hiburan, dan ciptakan batas pengeluaran yang sehat untuk tetap terjaga.</li>
                <li>Ingatlah untuk selalu mengalokasikan setidaknya 20% dari penghasilan Anda untuk tabungan dan investasi masa depan Anda di dalam anggaran Anda.</li>
                <li>Di halaman Anggaran, Anda dapat melacak sejauh mana Anda telah mencapai target alokasi keuangan Anda dalam setiap kategori.</li>
                <li>Anggaran ini akan membantu Anda memprioritaskan keuangan Anda dan menghindari pemborosan yang tidak perlu.</li>
                <li>Setiap kali Anda melakukan transaksi, pastikan untuk mengkategorikannya sesuai dengan prinsip alokasi keuangan yang telah Anda tetapkan.</li>
            </ul>
              
            <h4>Laporan</h4>
            <figure><img src="image/mobile (8).png" alt="">
                <figcaption></figcaption>
            </figure>
            <ul>
                <li>Halaman Laporan adalah tempat di mana Anda dapat melihat gambaran komprehensif tentang aktivitas keuangan Anda selama periode bulan tertentu.</li>
                <li>Anda dapat memilih rentang tanggal yang ingin Anda analisis untuk melihat laporan keuangan Anda.</li>
                <li>Laporan kami mencakup rincian pengeluaran, pemasukan, dan saldo keseluruhan Anda selama periode yang dipilih.</li>
                <li>Laporan juga mencantumkan perbandingan antara alokasi keuangan yang Anda rencanakan dan yang sebenarnya, membantu Anda untuk tetap berada dalam jalur keuangan yang benar.</li>
                <li>Gunakan laporan ini sebagai alat untuk mengevaluasi dan mengoptimalkan keuangan Anda, serta membuat rencana keuangan yang lebih baik untuk masa depan.</li>
              </ul>

